import { makeMLP } from '../src/lib/evolve/mlp.js'
import { evolve } from '../src/lib/evolve/ga.js'
import * as double from '../src/projects/pendulum/plants/double.js'
import fs from 'fs'

const ARCH = [8, 16, 12, 1], W = 8, V = 4
const half = double.PARAMS.trackHalfWidth, fMax = double.PARAMS.forceMax
const inputs = s => [Math.sin(s.theta1), Math.cos(s.theta1), Math.sin(s.theta2), Math.cos(s.theta2), s.theta1dot/W, s.theta2dot/W, s.x/half, s.xdot/V]
const wrap = a => { a=(a+Math.PI)%(2*Math.PI); if(a<0)a+=2*Math.PI; return a-Math.PI }
const DT = 1/120, T = 5
const STARTS = [0.06, 0.15, -0.12, 0.2].map(d => ({ theta1: d, theta2: -d*0.7 }))
const mlp = makeMLP(ARCH)
function evaluate(weights) {
  let total = 0
  for (const st of STARTS) {
    let s = { x:0, xdot:0, theta1:st.theta1, theta2:st.theta2, theta1dot:0, theta2dot:0 }, fit = 0
    for (let i=0;i<Math.round(T/DT);i++){
      let f = mlp.forward(weights, inputs(s))[0]*fMax; f=Math.max(-fMax,Math.min(fMax,f)); s=double.step(s,f,DT)
      fit += (0.25*(1+Math.cos(s.theta1))+0.25*(1+Math.cos(s.theta2)) - 0.02*Math.abs(f)/fMax - 0.04*Math.abs(s.x)/half)*DT
      if (Math.abs(wrap(s.theta1))>1.2 || Math.abs(wrap(s.theta2))>1.2){ fit-=1; break }
    }
    total += fit
  }
  return total/STARTS.length
}
const t0=Date.now()
const { best } = evolve({ paramCount:mlp.paramCount, randomParams:mlp.randomParams, mutate:mlp.mutate, evaluate,
  pop:72, gens:90, elite:6, tournamentK:4, mut:{rate:0.15,strength:0.35},
  onProgress:({gen,best})=>{ if(gen%30===0)console.log(`  gen ${gen}: best=${best.toFixed(3)}`) } })
console.log(`trained ${((Date.now()-t0)/1000).toFixed(1)}s, fitness ${best.fitness.toFixed(3)}`)
let s={x:0,xdot:0,theta1:0.15,theta2:-0.1,theta1dot:0,theta2dot:0}
for(let i=0;i<Math.round(10/DT);i++){ if(i===Math.round(4/DT))s.theta2dot+=1.0; let f=mlp.forward(best.weights,inputs(s))[0]*fMax; f=Math.max(-fMax,Math.min(fMax,f)); s=double.step(s,f,DT) }
const held=Math.abs(wrap(s.theta1))<0.1&&Math.abs(wrap(s.theta2))<0.1&&Math.abs(s.x)<half
console.log(`hold+nudge: th1=${(wrap(s.theta1)*180/Math.PI).toFixed(1)}° th2=${(wrap(s.theta2)*180/Math.PI).toFixed(1)}° x=${s.x.toFixed(2)} -> ${held?'HELD':'FELL'}`)
if (held && best.fitness>4){
  fs.writeFileSync('src/projects/pendulum/neural/champion-double.js',
`// Auto-generated by scratchpad/double-neural-train.mjs — pre-trained double-pole
// neural BALANCER (stabilizes near both-up + rejects small perturbations; does
// not swing up). Evolved via neuroevolution.
export const CHAMPION = {
  arch: [${ARCH.join(', ')}],
  fitness: ${best.fitness.toFixed(4)},
  weights: [${Array.from(best.weights).map(w=>w.toFixed(5)).join(', ')}],
}
`)
  console.log('saved -> champion-double.js')
} else console.log('NOT saved (unreliable)')
